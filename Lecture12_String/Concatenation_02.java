import java.util.*;

public class Concatenation_02{
    
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        String s = "Abhi";
        String s2 = "Bhai";
        String cct = s + " " + s2;
        System.out.println(cct);
        System.out.println(s+s2);
        System.out.println(s + " " +"@"+ " " + s2);
    }

}